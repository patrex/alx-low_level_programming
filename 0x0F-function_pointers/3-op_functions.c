#include "3-calc.h"

/**
 * op_add - adds and returns sum
 * @a: first number
 * @b: second number
 * Return: the sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - computes the difference
 * @a: first number
 * @b: second number
 * Return: the sum
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - computes the product
 * @a: first number
 * @b: second number
 * Return: the product
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - computes the dividend
 * @a: first number
 * @b: second number
 * Return: the dividend
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - computes the modulus
 * @a: first number
 * @b: second number
 * Return: the modulus
 */
int op_mod(int a, int b)
{
	return (a % b);
}
